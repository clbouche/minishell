#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	d = (char *)dst;
	s = (const char *)src;
	while (n != 0)
	{
		*d = (char)*s;
		s++;
		d++;
		n--;
	}
	return (dst);
}
